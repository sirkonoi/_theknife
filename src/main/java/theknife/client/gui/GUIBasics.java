package theknife.client.gui;

/**
 * Interfaccia GUIBasics
 * Definisce le costanti relative alle dimensioni usate nelle interfacce.
 * Vengono utilizzate per impostare la dimensione dei pulsanti, interfacce
 * etc...
 */

public interface GUIBasics {
    /**
     * Larghezza predefinita dello stage.
     */    
    public static final double WIDTH = 960;

    /**
     * Altezza predefinita dello stage.
     */
    public static final double HEIGHT = 540;

    /**
     * Larghezza minima per lo stage.
     */
    public static final double MIN_WIDTH = 600;

    /**
     * Altezza minima per lo stage.
     */
    public static final double MIN_HEIGHT = 400;

    /**
     * Larghezza definita per i field.
     */    
    public static final double FIELD_WIDTH = 440;

    /**
     * Larghezza definita per i bottoni.
     */
    public static final double BTN_WIDTH = 180;
}
